#include "fdf.h"

int	create_trgb(t_vec3d color)
{
	color = vec3d_clip(color, 0.0, 1.0);
	return (
		((int)(255 * color.x)) << 16
	| ((int)(255 * color.y)) << 8
	| ((int)(255 * color.z)));
}
